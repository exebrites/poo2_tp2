package unam.poo.modelo;

/**
 * Enumeración que representa el estado de un libro.
 *
 * @author camilagomez
 * @author exequielbrites
 * @author luisrapp
 *
 */
public enum EstadoLibro {
    /**
     * El libro está disponible para ser prestado.
     */
    DISPONIBLE,

    /**
     * El libro ha sido prestado y no está disponible.
     */
    PRESTADO
}